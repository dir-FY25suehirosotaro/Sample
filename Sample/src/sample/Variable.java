package sample;

public class Variable {

    public static void main(String[] args) {
        var nunber1 = 1;
        //var:数値変数の定義
        System.out.println(nunber1);

       String str1;
       str1 = "abc";
       //str:文字列変数の定義
       System.out.println(str1);
       //println:改行付きprint
       System.out.println();
       
       var secondsperday = 24*60*60;
       System.out.println(secondsperday);
       
    }

}
